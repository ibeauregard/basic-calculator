#include "operator.h"

int addition(int left, int right)
{
    return left + right;
}

int subtraction(int left, int right)
{
    return left - right;
}

int multiplication(int left, int right)
{
    return left * right;
}

int division(int left, int right)
{
    return left / right;
}

int modulo(int left, int right)
{
    return left % right;
}
